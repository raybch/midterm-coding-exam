package src.design;

public class Employees {

    private int employeeId;
    private  String employeeName;

    private int employeeCnic;

   private String employeeCountry;

   private String employeeDepartment;

   private  int totalPTO;

   private String  employeeTask;

   private  double employeeSalary;

   private  int employeeBonus;

   private  int employeeBenefit;



    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public int getEmployeeCnic() {
        return employeeCnic;
    }

    public void setEmployeeCnic(int employeeCnic) {
        this.employeeCnic = employeeCnic;
    }

    public String getEmployeeCountry() {
        return employeeCountry;
    }

    public void setEmployeeCountry(String employeeCountry) {
        this.employeeCountry = employeeCountry;
    }

    public String getEmployeeDepartment() {
        return employeeDepartment;
    }

    public void setEmployeeDepartment(String employeeDepartment) {
        this.employeeDepartment = employeeDepartment;
    }

    public int getTotalPTO() {
        return totalPTO;
    }

    public void setTotalPTO(int totalPTO) {
        this.totalPTO = totalPTO;
    }

    public String getEmployeeTask() {
        return employeeTask;
    }

    public void setEmployeeTask(String employeeTask) {
        this.employeeTask = employeeTask;
    }

    public double getEmployeeSalary() {
        return employeeSalary;
    }

    public void setEmployeeSalary(double employeeSalary) {
        this.employeeSalary = employeeSalary;
    }

    public int getEmployeeBonus() {
        return employeeBonus;
    }

    public void setEmployeeBonus(int employeeBonus) {
        this.employeeBonus = employeeBonus;
    }

    public int getEmployeeBenefit() {
        return employeeBenefit;
    }

    public void setEmployeeBenefit(int employeeBenefit) {
        this.employeeBenefit = employeeBenefit;
    }
}
